public class Ut4Act8 {
	
	public static void main(String args[]) {
		
		int number1Int = 100;
		int number2Int = 30;
		float number1Real = number1Int;
		float number2Real = number2Int;
		
		System.out.println("Integer:");
		System.out.println("Sum:"+(number1Int+number2Int));
		System.out.println("Subt:"+(number1Int-number2Int));
		System.out.println("Mult:"+(number1Int*number2Int));
		System.out.println("Div:"+(number1Int/number2Int));
		System.out.println("Mod:"+(number1Int%number2Int));
		System.out.println("-------------------------------------------------------");
		System.out.println("Real:");
		System.out.println("Sum:"+(number1Real+number2Real));
		System.out.println("Subt:"+(number1Real-number2Real));
		System.out.println("Mult:"+(number1Real*number2Real));
		System.out.println("Div:"+(number1Real/number2Real));
		System.out.println("Mod:"+(number1Real%number2Real));

	}

}