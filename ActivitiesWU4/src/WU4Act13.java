//implicit casting
public class WU4Act13 {
	public static void main(String[] args){
		//declare and initialize variables
		int a = 10, b, c;
		short s = 20;
		char car = 'k';
		float f;
		//operations
		b = car;
		f = a;
		c = s;
		//output
		System.out.println("El valor de car (tipo char) es "+car+" y ese valor en un int ha quedadp en "+b);
		System.out.println("El valor de a (tipo int) es "+a+" y ese valor en un float ha quedado en "+f);
		System.out.println("El valor de c (tipo short) es "+s+" y ese valor en un int se ha quedado en "+c);
	}
}
