//explicit casting and comments
public class WU4Act14 {
	public static void main(String[] args) {
		/**
		 * Statements
		 */
		int n;
		long c = 20;
		float ft = 2.4f;
		char k;
		byte s = 94;
		/*Explicit
		conversions */	
		n = (int) c;
		System.out.println(n);
		k = (char) s;
		n = (int) ft;
		System.out.println(k);
		System.out.println(n);
	}
}
